export interface Type {
  slot: number;
  type: {
    name: string;
    url: string;
  };
}

export interface Ability {
  ability: {
    name: string;
    url: string;
  };
  is_hidden: boolean;
  slot: number;
}

export interface Stat {
  base_stat: number;
  effort: number;
  stat: {
    name: string;
    url: string;
  };
}

export interface Pokemon {
  id: number;
  name: string;
  image: string;
  height: number;
  base_experience: number;
  color?: {
    name: string;
  };
  types: Type[];
  abilities: Ability[];
  stats: Stat[];
}
